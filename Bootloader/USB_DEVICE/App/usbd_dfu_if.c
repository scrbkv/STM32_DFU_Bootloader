/**
  * @brief  Erase sector.
  * @param  Add: Address of sector to be erased.
  * @retval 0 if operation is successful, MAL_FAIL else.
  */
uint16_t MEM_If_Erase_FS(uint32_t Add)
{
  /* USER CODE BEGIN 2 */

	if (Add < 0x90000000 || Add >= 0x90800000)
		return -1;
	Add -= QSPI_BASE;
	W25q_Erase_4k(Add);
	return 0;
  /* USER CODE END 2 */
}

/**
  * @brief  Memory write routine.
  * @param  src: Pointer to the source buffer. Address to be written to.
  * @param  dest: Pointer to the destination buffer.
  * @param  Len: Number of data to be written (in bytes).
  * @retval USBD_OK if operation is successful, MAL_FAIL else.
  */
uint16_t MEM_If_Write_FS(uint8_t *src, uint8_t *dest, uint32_t Len)
{
  /* USER CODE BEGIN 3 */
	uint32_t address = ((uint32_t)dest);
	if (address < 0x90000000 || address >= 0x90800000)
		return -1;
	address -= QSPI_BASE;

	while (Len >= 4096) {
		W25q_Write_4k(address, src);
		address += 4096;
		src += 4096;
		Len -= 4096;
	}

	if (Len > 0) {
		memset(tmpBuf, 0xFF, 4096);
		memcpy(tmpBuf, src, Len);
		W25q_Write_4k(address, tmpBuf);
	}
	return 0;
  /* USER CODE END 3 */
}

/**
  * @brief  Memory read routine.
  * @param  src: Pointer to the source buffer. Address to be written to.
  * @param  dest: Pointer to the destination buffer.
  * @param  Len: Number of data to be read (in bytes).
  * @retval Pointer to the physical address where data should be read.
  */
uint8_t *MEM_If_Read_FS(uint8_t *src, uint8_t *dest, uint32_t Len)
{
  /* Return a valid address to avoid HardFault */
  /* USER CODE BEGIN 4 */
	uint32_t address = ((uint32_t)src);
	if (address < 0x90000000 || address >= 0x90800000) {
		memset(dest, 0xEE, Len);
		return (uint8_t *) (dest);
	}
	address -= QSPI_BASE;

	uint8_t* d = dest;
	while (Len >= 4096) {
		W25q_Read_4k(address, dest);
		address += 4096;
		dest += 4096;
		Len -= 4096;
	}

	if (Len > 0) {
		W25q_Read_4k(address, tmpBuf);
		memcpy(dest, tmpBuf, Len);
	}

	/* Return a valid address to avoid HardFault */
	return (uint8_t *) (d);
  /* USER CODE END 4 */
}

/**
  * @brief  Get status routine
  * @param  Add: Address to be read from
  * @param  Cmd: Number of data to be read (in bytes)
  * @param  buffer: used for returning the time necessary for a program or an erase operation
  * @retval USBD_OK if operation is successful
  */
uint16_t MEM_If_GetStatus_FS(uint32_t Add, uint8_t Cmd, uint8_t *buffer)
{
  /* USER CODE BEGIN 5 */
	switch (Cmd)
	  {
	  case DFU_MEDIA_PROGRAM:
	    buffer[1] = (uint8_t) 50;
	    buffer[2] = (uint8_t) (50 << 8);
	    buffer[3] = 0;
	    break;

	  case DFU_MEDIA_ERASE:
	  default:
	    buffer[1] = (uint8_t) 50;
	    buffer[2] = (uint8_t) (50 << 8);
	    buffer[3] = 0;
	    break;
	  }
	  return 0;
  /* USER CODE END 5 */
}

